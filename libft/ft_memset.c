#include	"libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	x;
	char	*s;

	x = 0;
	s = b;
	while (x < len)
	{
		s[x] = c;
		x++;
	}
	return ((void *)s);
}
